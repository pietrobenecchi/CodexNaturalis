package it.polimi.ingsw.model;

/**
 * The ObjectiveType enum represents the different types of objectives that an ObjectiveCard can have in the game.
 *
 * STAIR: Represents the stair objective.
 * L_FORMATION: Represents the L formation objective.
 * FREE_RESOURCES: Represents the free resources objective.
 * TWO_QUILLS: Represents the two quills objective.
 * TWO_INKS: Represents the two inks objective.
 * TWO_SCROLLS: Represents the two scrolls objective.
 * TRIS: Represents the tris objective.
 */
public enum ObjectiveType {
    /**
     * The STAIR objective.
     */
    STAIR,
    /**
     * The L_FORMATION objective.
     */
    L_FORMATION,
    /**
     * The FREE_RESOURCES objective.
     */
    FREE_RESOURCES,
    /**
     * The TWO_QUILLS objective.
     */
    TWO_QUILLS,
    /**
     * The TWO_INKS objective.
     */
    TWO_INKS,
    /**
     * The TWO_SCROLLS objective.
     */
    TWO_SCROLLS,
    /**
     * The TRIS objective.
     */
    TRIS
}
