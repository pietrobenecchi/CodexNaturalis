package it.polimi.ingsw.model;

/**
 * This enum represents the countable items in the application.
 * The countable items available are INKWELL, QUILL, SCROLL, and CORNER.
 */
public enum Countable {
    /**
     * The INKWELL countable item.
     */
    INKWELL,
    /**
     * The QUILL countable item.
     */
    QUILL,
    /**
     * The SCROLL countable item.
     */
    SCROLL,
    /**
     * The CORNER countable item.
     */
    CORNER
}
